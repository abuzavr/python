# Декоратор для проверки результата функции на простое число
def is_prime(func):
    # Обертка, которая выполняет дополнительную логику проверки числа
    def wrapper(*args):
        # Вызываем оборачиваемую функцию и получаем результат
        result = func(*args)
        # Проверяем, является ли результат простым числом
        if result < 2:
            # Если число меньше 2, то оно составное (0 и 1 не являются простыми)
            print("Составное")
        else:
            # Проверяем делимость числа на все числа от 2 до корня из результата
            for i in range(2, int(result ** 0.5) + 1):
                if result % i == 0:
                    # Если нашли делитель, значит число составное
                    print("Составное")
                    break
            else:
                # Если цикл завершился без break, число простое
                print("Простое")
        # Возвращаем результат функции
        return result
    return wrapper  # Возвращаем обертку


@is_prime
def sum_three(a, b, c):
    # Возвращаем сумму трех чисел
    return a + b + c


result = sum_three(2, 3, 6)
print(result)


